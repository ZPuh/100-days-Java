package day10;
public class Day10 {
    public static void main(String[] args) {
        // OPERASI PERBANDINGAN 
        // Lebih Dari (>)
        // Kurang Dari (<)
        // Lebih dari sama dengan (>=)
        // Kurang dari sama dengan (<=)
        // Sama dengan (==)
        // Tidak sama dengan (!=)
        
        int nilai1 = 100;
        int nilai2 = 100;
        
        System.out.println(nilai1>nilai2);
        System.out.println(nilai1<nilai2);
        System.out.println(nilai1>=nilai2);
        System.out.println(nilai1<=nilai2);
        System.out.println(nilai1==nilai2);
        System.out.println(nilai1!=nilai2);
     }
    
}
