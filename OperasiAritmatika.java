package day7;
public class Day7 {
    public static void main(String[] args) {
    // Operasi Matematika
    // Penjumlahan    ( + )
    // Pengurangan    ( - )
    // Perkalian      ( * )
    // Pembagian      ( / )
    // Sisa Pembagian ( % )
    int a , b ;
    a = 1000;
    b = 10;
    System.out.println("=====OPERASI MATEMATIKA=====");
    System.out.println("===== A + B = "+(a+b)+"     =====");
    System.out.println("===== A - B = "+(a-b)+"      =====");
    System.out.println("===== A x B = "+(a*b)+"    =====");
    System.out.println("===== A : B = "+(a/b)+"      =====");
    System.out.println("===== A % B = "+(a%b)+"        =====");
    System.out.println("============================");
    
}}
